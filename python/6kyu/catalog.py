import re

def catalog(s, article):

  lines = s.split('\n')
  res = []

  for line in lines:
    if (article in line):
      name = re.findall("<name>(.*?)<\/name>", line)[0]
      price = re.findall("<prx>(.*?)<\/prx>", line)[0]
      quantity = re.findall("<qty>(.*?)<\/qty>", line)[0]
      res.append('{} > prx: ${} qty: {}'.format(name, price, quantity))
  
  if (len(res) == 0):
    return 'Nothing'

  if (len(res) == 1):
    return ''.join(res)

  else:
    return '\r\n'.join(res)





